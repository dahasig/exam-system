def add_question(session, level, qtype, question, options=None, correct="A"):
    options = options or ["", "", "", ""]
    session.add(
        Question(
            category="support",
            level=level,
            qtype=qtype,
            question=question,
            option_a=options[0],
            option_b=options[1],
            option_c=options[2],
            option_d=options[3],
            correct_option=correct,
        )
    )


def run():
    create_db_and_tables()
    session = next(get_session())

    session.query(User).delete()
    session.query(Question).delete()

    session.add(User(username="nimda", password="nimda"))

    letters = ["A", "B", "C", "D"]

    for i in range(150):
        topic, correct_text, wrongs = TOPICS[i % len(TOPICS)]
        phrase = PHRASES[i % len(PHRASES)]
        level = "medium" if i < 75 else "hard"

        question = f"{phrase} {topic}؟"

        correct_letter = letters[i % 4]
        options = ["", "", "", ""]
        options[letters.index(correct_letter)] = correct_text

        w = 0
        for idx in range(4):
            if options[idx] == "":
                options[idx] = wrongs[w]
                w += 1

        add_question(session, level, "mcq", question, options, correct_letter)

    for level, q in ESSAY:
        add_question(session, level, "essay", q)

    session.commit()
    session.close()

