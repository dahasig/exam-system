def add_question(session, level, qtype, question, options=None, correct="A"):
    existing = session.exec(
        select(Question).where(
            Question.question == question,
            Question.level == level,
            Question.qtype == qtype
        )
    ).first()

    if existing:
        return

    options = options or ["", "", "", ""]
    session.add(
        Question(
            category="support",
            level=level,
            qtype=qtype,
            question=question,
            option_a=options[0],
            option_b=options[1],
            option_c=options[2],
            option_d=options[3],
            correct_option=correct,
        )
    )


